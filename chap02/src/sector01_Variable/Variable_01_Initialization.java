package sector01_Variable;

public class Variable_01_Initialization {
    public static void main(String[] args) {

        int value; //변수 value 선언
        value = 10;


        int result = value + 10;
        // 연산의 결과값을 변수 result의 초기값으로 저장
        //          =
        // 변수 value 값을 읽어들여 10을 더하는 연산 수행

        System.out.println(result);
        // 변수 result 값 읽어 콘솔에 출력
    }
}
